#include "../include/minishell.h"


int	check_pipe(char **cmd)
{
	int	i;
	
	i = 1;

	while (cmd[i])
	{
		if (ft_strncmp(cmd[i], "|", 1) == 0)
			return (i);
		i++;
	}
	return (0);
}
